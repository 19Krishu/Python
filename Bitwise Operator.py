#Bitwise Operator
"""
or    |
and   &
not   ~
xor   ^
shift left   <<
shift right  >>

"""
#AND

a=2
b=4
print(a & b)
print(bin(a))
print(bin(b))
"""
--Bitwise AND--
8421
0010=2 a
0100=4 b

0000=0 o/p

"""
#OR

c=4
d=5
print(c | d)
print(bin(c))
print(bin(d))
"""
--Bitwise OR--
8421
0010=4 c
0101=5 d

0101=5 o/p

"""
#XOR

e=4
f=6
print(e ^ f)
print(bin(e))
print(bin(f))
"""
--Bitwise XOR--

0100=4 e
0110=6 f
0010=2 o/p

"""
#SHIFT LEFT

g=3
print(g << 1)
print(bin(g))
"""

0011=3 g
0110=6 o/p

"""
#SHIFT RIGHT

h=4
print(g >> 1)
print(bin(h))
"""

0011=3 h
0110=1 o/p

"""

